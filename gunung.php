  <p>Gunung Bromo, siapa yang tidak mengenal kepopuleran gunung berapi yang masih aktif ini. Gunung Bromo adalah gunung yang paling terkenal di Jawa Timur dengan kunjungan yang paling ramai setiap tahunnya.<br>

Gunung Bromo memiliki ketinggian 2.392 Meter dari atas permukaan laut dan berada dalam empat lingkup kabupaten, yaitu Probolinggo, Pasuruan, Lumajang dan Kabupaten Malang. Keadaan alam gunung Bromo bertautan pula dengan lembah, ngarai, caldera atau lautan pasir dengan luas sekitar 10 Km.<br>

Gunung Bromo juga termasuk dalam satu kawasan Bromo Tengger Semeru National Park, dimana terdapat beberapa obyek wisata yang bisa dikunjungi seperti, Gunung Semeru, Gunung Tengger, Gunung Batok, beberapa danau dan Gunung Bromo sendiri.<br>

Sebagai gunung berapi yang masih aktif, Gunung Bromo telah mengalami letusan dengan interval waktu yang teratur dalam 20 abad ini, yakni sekitar 30 tahun sekali. Letusan terbesar terjadi pada tahun 1974 dan kembali meletus di tahun 2010.<br>

Selain keindahan yang tersimpan di Gunung Bromo, Yadna Kasada atau Upacara Kasodo lah yang membuat Gunung Bromo menjadi tujuan destinasi utama setiap tahunnya. Upacara Kasodo digelar setiap tahun pada bulan purnama di bulan Desember atau January.<br>

Asal muasal Upacara Kasodo ini bermula sejak abad ke-15 di mana diceritakan tentang seorang putri bernama Roro Anteng yang memimpin kerajaan Tengger dengan suaminya, Joko Seger. Pasangan ini tidak memiliki anak dan karena itu mereka berdoa dan memohon kepada dewa-dewa gunung untuk diberikan anak.<br>

Dari permohonan mereka, dewa memberi 24 anak dan mewajibkan bagi mereka untuk mengorbankan anak ke 25 mereka untuk dilempar kedalam gunung berapi. Permintaan dewa inipun dilaksanakan sehingga menjadi tradisi sampai saat ini. Rakyat Tengger melakukan upacara Kasada dengan melemparkan hasil bumi ke dalam kawah Bromo sebagai ucapan syukur atas panen yang diterima dan sebagai permohonan untuk panen yang lebih melimpah di musim selanjutnya.<br>

Meskipun penuh dengan bahaya, terdapat beberapa penduduk setempat yang mengambil resiko dengan naik dan turun ke kawah dalam upaya untuk mengambil kembali barang yang dikorbankan yang diyakini bisa membawa keberuntungan.</p>


