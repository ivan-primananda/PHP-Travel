  <p>Gunung Arjuno (terkadang dieja Gunung Arjuna) adalah sebuah gunung berapi kerucut (istirahat) di Jawa Timur, Indonesia dengan ketinggian 3.339 m dpl. Gunung Arjuno secara administratif terletak di perbatasan Kota Batu, Kabupaten Malang, dan Kabupaten Pasuruan dan berada di bawah pengelolaan Taman Hutan Raya Raden Soerjo. Gunung Arjuno merupakan gunung tertinggi ketiga di Jawa Timur setelah Gunung Semeru dan Gunung Raung, serta menjadi yang tertinggi keempat di Pulau Jawa. Biasanya gunung ini dicapai dari tiga titik pendakian yang cukup dikenal yaitu dari Lawang, Tretes dan Batu. Nama Arjuno berasal dari salah satu tokoh pewayangan Mahabharata, Arjuna.<br>

Gunung Arjuno bersebelahan dengan Gunung Welirang, Gunung Kembar I, dan Gunung Kembar II. Puncak Gunung Arjuno terletak pada satu punggungan yang sama dengan puncak gunung Welirang, sehingga kompleks ini sering disebut juga dengan Arjuno-Welirang. Kompleks Arjuno-Welirang sendiri berada di dua gunung berapi yang lebih tua, Gunung Ringgit di timur dan Gunung Lincing di selatan. Area fumarol dengan cadangan belerang ditemukan di sejumlah lokasi pegunungan ini, seperti pada puncak Gunung Welirang, puncak Gunung Kembar II, dan pada sejumlah jalur pendakian.<br>

Gunung Arjuno merupakan salah satu tujuan pendakian. Di samping tingginya yang telah mencapai lebih dari 3000 meter, di gunung ini terdapat beberapa objek wisata. Salah satunya adalah objek wisata air terjun Kakek Bodo yang juga merupakan salah satu jalur pendakian menuju puncak Gunung Arjuno. Meskipun selain objek wisata air terjun Kakek Bodo terdapat pula air terjun lain, tetapi para wisatawan jarang yang mendatangi air terjun lainnya, mungkin karena letak dan sarana wisatanya kurang mendukung. Di kawasan lerengnya juga terdapat mata air Sungai Brantas yang berasal dari simpanan air Gunung Arjuno. Mata air Sungai Brantas terletak di Desa Sumber Brantas, Bumiaji, Kota Batu yang merupakan sungai terpanjang kedua di Pulau Jawa setelah Sungai Bengawan Solo. Beberapa destinasi wisata yang terkenal hingga ke seluruh Indonesia maupun luar negeri juga terletak di lereng Gunung Arjuno, di antaranya adalah Tretes, Kota Wisata Batu, dan Taman Safari Indonesia 2.<br>

Gunung Arjuno mempunyai kawasan hutan Dipterokarp Bukit, hutan Dipterokarp Atas, hutan Montane, dan Hutan Ericaceous atau hutan gunung.

Gunung Arjuno dapat didaki dan berbagai arah, arah Utara (Tretes) melalui Gunung Welirang, dan arah Timur (Lawang) dan dari arah Barat (Batu-Selecta), dan arah selatan (Karangploso), juga dari Sumberawan, Singosari. Desa Sumberawan adalah desa pusat kerajinan tangan di kecamatan Singosari, Kabupaten Malang dan merupakan desa terakhir untuk mempersiapkan diri sebelum memulai pendakian. Bisa juga melewati Purwosari yang lebih gampang dilewati, karena hanya setengah jam dari jalan raya dan langsung sampai di Tambakwatu.</p>

