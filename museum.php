      <p>Jika Anda ingin berekreasi sambil belajar, Malang Raya menyediakan banyak lokasi yang bisa dipilih sebagai destinasi. Museum Zoologi Frater Vianney salah satunya, yang mungkin bisa Anda coba kunjungi.<br>
      
      Banyak museum di Malang, namun Anda mungkin jarang mendengar nama Museum Zoologi Frater Vianney ini. Museum ini berada dalam satu kompleks dengan Provinsialat Frater BHK, Novisiat Frater BHK, Kantor Pusat Yayasan Pendidikan Mardi Wiyata, dan makam Frater BHK.<br>
      
      Lokasinya cukup strategis, karena tak jauh dari Kota Malang. Museum ini terletak di Jl. Raya Karangwidoro No. 7 Desa Karangbesuki, Kecamatan Dau, Kabupaten Malang. Dari arah kota, silakan Anda menuju ke arah barat, melewati Terminal Landungsari.<br>
      
      Ratusan koleksi spesimen konkologi (ilmu hewan kerang-kerangan darat dan laut) terpampang nyata di museum zoologi ini. Terdapat pula koleksi spesimen herpetologi (ilmu tentang biologi ular). Menariknya, ratusan koleksi itu sudah terklasifikasi secara lengkap. Menurut sang Direktur Museum, Frater Maria Clemens BHK, ratusan koleksi itu merupakan sejatinya peninggalan almarhum Frater Vianney. Dia adalah biarawan kelahiran Belanda yang berkarya di Indonesia sejak tahun 1960-an. Tak kurang dari 80 famili hewan mollusca yang ada di Indonesia berhasil dikoleksinya.<br>
      
      Ratusan spesimen konkologi yang terawat dengan baik itu kemudian disumbangkan ke sebuah Perguruan Tinggi. Koleksi milik Frater Vianney yang terpisah di Malang dan Flores itu berhasil dikumpulkan kembali dan disimpan dalam museum ini sejak tahun 1998.
      
      Selain itu, museum ini juga memiliki puluhan jenis ular. Ular-ular itu di antaranya ada yang dalam bentuk awetan, ada pula yang masih hidup hingga sekarang. Hewan-hewan melata itu juga ‘warisan’ sang frater.<br>
      
      Saat ini, Museum Zoologi Frater Vianney ini telah bekerjasama dengan Balai Konservasi Sumber Daya Alam (BKSDA) Jawa Timur. Mereka menampung titipan hewan untuk dirawat. Untuk pengelolaan Museum zoologi ini, Clemens tidak sendiri. Ia dibantu empat orang karyawan, dengan biaya perawatan dari yayasan dan tiket masuk pengunjung.<br>
      
      Perawatan koleksi museum ini memang bergantung dari pendapatan tiket masuk para pengunjung. Pihak pengelola menentukan tarif tiket masuk berdasarkan jenjang sekolah. Untuk siswa SD dikenakan tiket 10.000 rupiah, SMP 12.500 rupiah, SMA 15.000 rupiah, dan Perguruan Tinggi 20.000 rupiah. Khusus untuk pengunjung yang datang bersama keluarga, pengelola membebaskan berapa pun yang ingin disumbangkan. Hal tersebut sesuai dengan prinsip didirikannya museum ini dengan tujuan untuk sarana belajar.</p>

