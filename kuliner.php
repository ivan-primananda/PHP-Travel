  Sebagai sebuah restoran jadul, Toko Oen bisa dibilang menyajikan kuliner khas Belanda yang lengkap. Di sebuah counter berbentuk persegi panjang di depan pintu masuk terdapat berbagai jenis roti dan kue kering khas Belanda seperti speculaas, havermout, dan kaastengel. Untuk menyesuaikan dengan perkembangan zaman, kue-kue basah khas Indonesia turut ditawarkan di etalase yang sama dengan roti-roti.</p>

